function charToCP1251(c) {
    let code = c.charCodeAt(0);
    if (code <= 0x7F) return code;
    if (code >= 0x0410 && code <= 0x044F) return code - 0x0410 + 0xC0; // Cyrillic А-я
    if (map1251[code]) return map1251[code];
    return 0x3F; // '?'
}

let text = fs.readFileSync('script.js', 'utf8');
let bytes = Buffer.alloc(text.length);
let outLen = 0;
for (let i = 0; i < text.length; i++) {
    bytes[outLen++] = charToCP1251(text[i]);
}
let origText = bytes.slice(0, outLen).toString('utf8');
fs.writeFileSync('script.js', origText, 'utf8');
console.log("Done");
